package com.coding.day06.系统方法调用;

public class Demo1 {

    public static void main(String[] args) {
        System.out.println(Math.abs(-4.565));//取绝对值
        System.out.println(Math.min(656.16,56565.1515));//求两数之间较小值
        System.out.println(Math.min(656.16,56565.1515));//求两数之间较大值
        System.out.println(Math.ceil(-1.565));//向下取整
        System.out.println(Math.floor(-1.565));//向上取整
        System.out.println(Math.pow(9,-0.5));//求一个数的n次幂
        System.out.println(Math.sqrt(9));//求开平方
    }
}
